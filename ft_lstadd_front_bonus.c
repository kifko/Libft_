#include "libft.h"

// ##############################################################################
// DESCRIPTION: Añade el nodo ’new’ al principio de la lista ’lst’.
// ##############################################################################
// RETURN VALUE: Nada
// ##############################################################################

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	new->next = *lst;
	*lst = new;
}
