#include "libft.h"

/*
// ##############################################################################
// DESCRIPTION: Cuenta el nÃºmero de nodos de una lista.
// ##############################################################################
// RETURN VALUE: La longitud de la lista.
// ##############################################################################
*/

int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	while (lst != NULL)
	{
		lst = lst->next;
		count++;
	}
	return (count);
}
