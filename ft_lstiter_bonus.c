#include "libft.h"

// ##############################################################################
// DESCRIPTION: Itera la lista ’lst’ y aplica la función ’f’ en el
// contenido de cada nodo.
// ##############################################################################
// RETURN VALUE: Nada
// ##############################################################################

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
